// Translate function
function applyLanguage(lang) {
  if (!translations[lang]) return;
  
  // Set in localStorage
  localStorage.setItem('kits_lang', lang);
  
  const dictionary = translations[lang];

  // Update elements with data-translate attribute
  document.querySelectorAll('[data-translate]').forEach(el => {
    const key = el.getAttribute('data-translate');
    if (dictionary[key]) {
      el.innerHTML = dictionary[key];
    }
  });

  // Update element placeholders with data-translate-placeholder attribute
  document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
    const key = el.getAttribute('data-translate-placeholder');
    if (dictionary[key]) {
      el.placeholder = dictionary[key];
    }
  });

  // Update active state in desktop dropdown select options
  document.querySelectorAll('.lang-option').forEach(opt => {
    if (opt.getAttribute('data-lang') === lang) {
      opt.classList.add('active');
    } else {
      opt.classList.remove('active');
    }
  });

  // Update active state in mobile segmented selectors
  document.querySelectorAll('.btn-mobile-lang').forEach(btn => {
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('active');
    } else {
      btn.classList.remove('active');
    }
  });

  // Update all header language text fields to upper case code
  document.querySelectorAll('.btn-lang span').forEach(span => {
    span.textContent = lang.toUpperCase();
  });

  // Synchronize HTML lang attribute
  document.documentElement.setAttribute('lang', lang);
}

// Translate Helper for dynamic strings
function t(key, defaultVal = '') {
  const currentLang = localStorage.getItem('kits_lang') || 'en';
  return (translations[currentLang] && translations[currentLang][key]) || defaultVal;
}

